	static class Solver
	{
		int n, m, arr[], pos[];
		InputReader in;
		OutputWriter out;

		/**
		 * Solution from the right end of the array.
		 * @param testNumber
		 */
		void solve(int testNumber)
		{
			n = in.nextInt();
			m = in.nextInt();
			arr = new int[n + 1];
			pos = new int[n + 1];

			for (int i = 1; i <= n; i++)
			{
				arr[i] = in.nextInt();
				pos[arr[i]] = i;
			}

			int[] next = new int[n + 1];

			Arrays.fill(next, n + 1);
			next[0] = 1;

			for (int i = 0; i < m; i++)
			{
				int a, b;

				a = in.nextInt();
				b = in.nextInt();

				if (pos[a] > pos[b])
				{
					int temp = a;

					a = b;
					b = temp;
				}

				next[pos[a]] = Math.min(next[pos[a]], pos[b]);
			}

			int right = n;
			long answer = 0;

			for (int left = n; left >= 0; left--)
				while (right >= next[left])
					answer += right-- - left;

			out.println(answer);
		}

		/**
		 * Same solution from the left end of the array.
		 * @param testNumber
		 */
		void solve2(int testNumber)
		{
			n = in.nextInt();
			m = in.nextInt();
			arr = new int[n];
			pos = new int[n];

			for (int i = 0; i < n; i++)
			{
				arr[i] = in.nextInt() - 1;
				pos[arr[i]] = i;
			}

			int[] nearestLeft = new int[n + 1];

			Arrays.fill(nearestLeft, -1);
			nearestLeft[n] = n - 1;

			for (int i = 0; i < m; i++)
			{
				int a, b;

				a = in.nextInt() - 1;
				b = in.nextInt() - 1;

				if (pos[a] > pos[b])
				{
					int temp = a;

					a = b;
					b = temp;
				}

				nearestLeft[pos[b]] = Math.max(nearestLeft[pos[b]], pos[a]);
			}

			int left = 0;
			long answer = 0;

			for (int right = 0; right <= n; right++)
				while (left <= nearestLeft[right])
					answer += right - left++;

			out.println(answer);
		}

		public Solver(InputReader in, OutputWriter out)
		{
			this.in = in;
			this.out = out;
		}

	}

	static class InputReader
	{
		private InputStream stream;
		private byte[] buf = new byte[1024];
		private int curChar;
		private int numChars;

		public InputReader(InputStream stream)
		{
			this.stream = stream;
		}

		public int read()
		{
			if (numChars == -1)
				throw new InputMismatchException();

			if (curChar >= numChars)
			{
				curChar = 0;
				try
				{
					numChars = stream.read(buf);
				}
				catch (IOException e)
				{
					throw new InputMismatchException();
				}
				if (numChars <= 0)
					return -1;
			}

			return buf[curChar++];
		}

		public int nextInt()
		{
			int c = read();

			while (isSpaceChar(c))
				c = read();

			int sgn = 1;

			if (c == '-')
			{
				sgn = -1;
				c = read();
			}

			int res = 0;

			do
			{
				if (c < '0' || c > '9')
					throw new InputMismatchException();

				res *= 10;
				res += c & 15;

				c = read();
			} while (!isSpaceChar(c));

			return res * sgn;
		}

		public int[] nextIntArray(int arraySize)
		{
			int array[] = new int[arraySize];

			for (int i = 0; i < arraySize; i++)
				array[i] = nextInt();

			return array;
		}

		public long nextLong()
		{
			int c = read();

			while (isSpaceChar(c))
				c = read();

			int sign = 1;

			if (c == '-')
			{
				sign = -1;

				c = read();
			}

			long result = 0;

			do
			{
				if (c < '0' || c > '9')
					throw new InputMismatchException();

				result *= 10;
				result += c & 15;

				c = read();
			} while (!isSpaceChar(c));

			return result * sign;
		}

		public long[] nextLongArray(int arraySize)
		{
			long array[] = new long[arraySize];

			for (int i = 0; i < arraySize; i++)
				array[i] = nextLong();

			return array;
		}

		public float nextFloat() // problematic
		{
			float result, div;
			byte c;

			result = 0;
			div = 1;
			c = (byte) read();

			while (c <= ' ')
				c = (byte) read();

			boolean isNegative = (c == '-');

			if (isNegative)
				c = (byte) read();

			do
			{
				result = result * 10 + c - '0';
			} while ((c = (byte) read()) >= '0' && c <= '9');

			if (c == '.')
				while ((c = (byte) read()) >= '0' && c <= '9')
					result += (c - '0') / (div *= 10);

			if (isNegative)
				return -result;

			return result;
		}

		public double nextDouble() // not completely accurate
		{
			double ret = 0, div = 1;
			byte c = (byte) read();

			while (c <= ' ')
				c = (byte) read();

			boolean neg = (c == '-');

			if (neg)
				c = (byte) read();

			do
			{
				ret = ret * 10 + c - '0';
			} while ((c = (byte) read()) >= '0' && c <= '9');

			if (c == '.')
				while ((c = (byte) read()) >= '0' && c <= '9')
					ret += (c - '0') / (div *= 10);

			if (neg)
				return -ret;

			return ret;
		}

		public String next()
		{
			int c = read();

			while (isSpaceChar(c))
				c = read();

			StringBuilder res = new StringBuilder();

			do
			{
				res.appendCodePoint(c);

				c = read();
			} while (!isSpaceChar(c));

			return res.toString();
		}

		public boolean isSpaceChar(int c)
		{
			return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
		}

		public String nextLine()
		{
			int c = read();

			StringBuilder result = new StringBuilder();

			do
			{
				result.appendCodePoint(c);

				c = read();
			} while (!isNewLine(c));

			return result.toString();
		}

		public boolean isNewLine(int c)
		{
			return c == '\n';
		}

		public void close()
		{
			try
			{
				stream.close();
			}
			catch (IOException e)
			{
				e.printStackTrace();
			}
		}

	}

